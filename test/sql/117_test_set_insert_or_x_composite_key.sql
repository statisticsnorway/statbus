SELECT sql_saga.add_foreign_key('set_test_composite.stat_for_unit', ARRAY['legal_unit_id'], 'valid', 'legal_unit_id_uk');

-- Helper procedure to reset the target table for a new scenario
CREATE OR REPLACE PROCEDURE set_test_composite.reset_target() AS $$
BEGIN
    TRUNCATE set_test_composite.stat_definition, set_test_composite.legal_unit, set_test_composite.establishment, set_test_composite.stat_for_unit, set_test_composite.stat_for_unit_with_id, set_test_composite.stat_for_unit_with_identity, set_test_composite.stat_for_unit_no_pk RESTART IDENTITY CASCADE;
    -- Add parent data with more realistic IDs
    INSERT INTO set_test_composite.stat_definition (id, name) VALUES
    (10, 'Total Employees');
    INSERT INTO set_test_composite.legal_unit (id, valid_after, valid_to, name) VALUES
    (200, '1900-01-01', 'infinity', 'LU 200');
    INSERT INTO set_test_composite.establishment (id, valid_after, valid_to, name) VALUES
    (100, '1900-01-01', 'infinity', 'EST 100');
END;
$$ LANGUAGE plpgsql;

