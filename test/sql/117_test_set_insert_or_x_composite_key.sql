-- Setup: Create necessary schema and tables for this test
CREATE SCHEMA IF NOT EXISTS set_test_composite;

-- Parent tables
CREATE TABLE set_test_composite.stat_definition (
    id INT PRIMARY KEY,
    name TEXT
);
CREATE TABLE set_test_composite.legal_unit (
    id INT NOT NULL,
    valid_after DATE NOT NULL,
    valid_to DATE NOT NULL,
    name TEXT,
    PRIMARY KEY (id, valid_after)
);
SELECT sql_saga.add_era('set_test_composite.legal_unit', 'valid_after', 'valid_to');
SELECT sql_saga.add_unique_key('set_test_composite.legal_unit', ARRAY['id'], 'valid', 'legal_unit_id_uk');

CREATE TABLE set_test_composite.establishment (
    id INT NOT NULL,
    valid_after DATE NOT NULL,
    valid_to DATE NOT NULL,
    name TEXT,
    PRIMARY KEY (id, valid_after)
);
