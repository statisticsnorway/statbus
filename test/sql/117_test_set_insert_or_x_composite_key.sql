SELECT sql_saga.add_unique_key('set_test_composite.establishment', ARRAY['id'], 'valid', 'establishment_id_uk');

-- Target table: stat_for_unit (models public.stat_for_unit)
CREATE TABLE set_test_composite.stat_for_unit (
    stat_definition_id INT NOT NULL,
    establishment_id INT,
    legal_unit_id INT,
    valid_after DATE NOT NULL,
    valid_to DATE NOT NULL,
    value BIGINT,
    edit_comment TEXT, -- Ephemeral column
    -- No single primary key, temporal key is composite + valid_after
    CONSTRAINT one_and_only_one_unit_must_be_set
        CHECK ((establishment_id IS NOT NULL AND legal_unit_id IS NULL) OR (establishment_id IS NULL AND legal_unit_id IS NOT NULL))
);
SELECT sql_saga.add_era('set_test_composite.stat_for_unit', 'valid_after', 'valid_to');
-- The composite keys that identify conceptual entities over time
SELECT sql_saga.add_unique_key('set_test_composite.stat_for_unit', ARRAY['stat_definition_id', 'establishment_id'], 'valid', 'stat_for_unit_est_uk');
SELECT sql_saga.add_unique_key('set_test_composite.stat_for_unit', ARRAY['stat_definition_id', 'legal_unit_id'], 'valid', 'stat_for_unit_lu_uk');

-- Target table that models stat_for_unit even more closely with a surrogate ID
CREATE TABLE set_test_composite.stat_for_unit_with_id (
    id SERIAL PRIMARY KEY, -- Surrogate key with a default
    stat_definition_id INT NOT NULL,
    establishment_id INT,
    valid_after DATE NOT NULL,
    valid_to DATE NOT NULL,
    value BIGINT,
    edit_comment TEXT
);
SELECT sql_saga.add_era('set_test_composite.stat_for_unit_with_id', 'valid_after', 'valid_to');
SELECT sql_saga.add_unique_key('set_test_composite.stat_for_unit_with_id', ARRAY['stat_definition_id', 'establishment_id'], 'valid', 'stat_for_unit_with_id_uk');

-- Target table with surrogate key using IDENTITY, also modeling stat_for_unit
CREATE TABLE set_test_composite.stat_for_unit_with_identity (
    id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY, -- Surrogate key with IDENTITY
    stat_definition_id INT NOT NULL,
    establishment_id INT,
    valid_after DATE NOT NULL,
    valid_to DATE NOT NULL,
    value BIGINT,
    edit_comment TEXT
);
SELECT sql_saga.add_era('set_test_composite.stat_for_unit_with_identity', 'valid_after', 'valid_to');
SELECT sql_saga.add_unique_key('set_test_composite.stat_for_unit_with_identity', ARRAY['stat_definition_id', 'establishment_id'], 'valid', 'stat_for_unit_with_identity_uk');

-- Target table mimicking stat_for_unit (id is NOT NULL w/ DEFAULT, but NOT a PK)
CREATE SEQUENCE set_test_composite.stat_for_unit_no_pk_id_seq;
CREATE TABLE set_test_composite.stat_for_unit_no_pk (
    id INT NOT NULL DEFAULT nextval('set_test_composite.stat_for_unit_no_pk_id_seq'),
    stat_definition_id INT NOT NULL,
    establishment_id INT,
    valid_after DATE NOT NULL,
    valid_to DATE NOT NULL,
    value BIGINT,
    edit_comment TEXT,
    UNIQUE (id, valid_after) -- Mimics unique constraint on stat_for_unit
);
SELECT sql_saga.add_era('set_test_composite.stat_for_unit_no_pk', 'valid_after', 'valid_to');
SELECT sql_saga.add_unique_key('set_test_composite.stat_for_unit_no_pk', ARRAY['stat_definition_id', 'establishment_id'], 'valid', 'stat_for_unit_no_pk_uk');

