BEGIN;

CREATE TYPE public.activity_type AS ENUM ('primary', 'secondary', 'ancilliary');

CREATE TABLE public.activity (
    id integer GENERATED BY DEFAULT AS IDENTITY,
    valid_range daterange NOT NULL,
    valid_from date NOT NULL,
    valid_to date,
    valid_until date,
    type public.activity_type NOT NULL,
    category_id integer NOT NULL REFERENCES public.activity_category(id) ON DELETE CASCADE,
    data_source_id integer REFERENCES public.data_source(id) ON DELETE SET NULL,
    edit_comment character varying(512),
    edit_by_user_id integer NOT NULL REFERENCES auth.user(id) ON DELETE RESTRICT,
    edit_at timestamp with time zone NOT NULL DEFAULT statement_timestamp(),
    establishment_id integer,
    legal_unit_id integer,
    CONSTRAINT "One and only one statistical unit id must be set"
    CHECK( establishment_id IS NOT NULL AND legal_unit_id IS     NULL
        OR establishment_id IS     NULL AND legal_unit_id IS NOT NULL
        )
);
CREATE INDEX ix_activity_type ON public.activity USING btree (type);
CREATE INDEX ix_activity_category_id ON public.activity USING btree (category_id);
CREATE INDEX ix_activity_establishment_id ON public.activity USING btree (establishment_id);
CREATE INDEX ix_activity_legal_unit_id ON public.activity USING btree (legal_unit_id);
CREATE INDEX ix_activity_edit_by_user_id ON public.activity USING btree (edit_by_user_id);
CREATE INDEX ix_activity_data_source_id ON public.activity USING btree (data_source_id);
CREATE INDEX ix_activity_establishment_valid_from_valid_until ON public.activity USING btree (establishment_id, valid_from, valid_until);
CREATE INDEX ix_activity_legal_unit_id_valid_range ON public.activity USING gist (legal_unit_id, valid_range);
CREATE INDEX ix_activity_establishment_id_valid_range ON public.activity USING gist (establishment_id, valid_range);
CREATE INDEX ix_activity_valid_range ON public.activity USING gist (valid_range);

-- Improve filtering for temporal_merge
-- Note: Column order must be alphabetical to match sql_saga's lookup_columns expectation
CREATE INDEX ON activity (establishment_id, legal_unit_id, type);
CREATE INDEX ON activity (establishment_id, type) WHERE legal_unit_id IS NULL;
CREATE INDEX ON activity (legal_unit_id, type) WHERE establishment_id IS NULL;

-- Activate era handling with valid_range as the authoritative column.
-- The trigger will synchronize valid_from, valid_until, and valid_to from valid_range.
SELECT sql_saga.add_era('public.activity', 'valid_range');
-- This creates a PRIMARY KEY with WITHOUT OVERLAPS to ensure that there are
-- no overlapping time periods for the same activity ID.
SELECT sql_saga.add_unique_key(
    table_oid => 'public.activity'::regclass,
    key_type => 'primary',
    column_names => ARRAY['id'],
    unique_key_name => 'activity_id_valid'
);
-- This creates a GIST exclusion constraint (`activity_type_establishment_id_valid_excl`) to ensure
-- a unit cannot have multiple activities of the same type at the same time.
SELECT sql_saga.add_unique_key(
    table_oid => 'public.activity'::regclass,
    key_type => 'natural',
    column_names => ARRAY['type', 'legal_unit_id', 'establishment_id'],
    mutually_exclusive_columns => ARRAY['legal_unit_id', 'establishment_id'],
    unique_key_name => 'activity_type_establishment_id_valid'
);

-- This creates triggers to enforce that an activity's validity period is always contained
-- within the validity period of its parent establishment.
SELECT sql_saga.add_foreign_key(
    fk_table_oid => 'public.activity'::regclass,
    fk_column_names => ARRAY['establishment_id'],
    pk_table_oid => 'public.establishment',
    pk_column_names => ARRAY['id']
);
-- This creates triggers to enforce that an activity's validity period is always contained
-- within the validity period of its parent legal unit.
SELECT sql_saga.add_foreign_key(
    fk_table_oid => 'public.activity'::regclass,
    fk_column_names => ARRAY['legal_unit_id'],
    pk_table_oid => 'public.legal_unit',
    pk_column_names => ARRAY['id']
);

-- Add a view for portion-of updates, allowing for easier updates to specific time slices.
SELECT sql_saga.add_for_portion_of_view('public.activity');

END;
